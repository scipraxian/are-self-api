class GenomePreviewLoaderTest(_SaveAsHarness):
    """Fix #5 — :func:`genome_preview` returns the live read-only snapshot."""

    def test_preview_includes_manifest_and_row_counts(self):
        """Assert manifest + per-model row counts are populated."""
        preview = loader.genome_preview('sourcer')

        self.assertEqual(preview['slug'], 'sourcer')
        self.assertEqual(
            preview['manifest']['slug'], 'sourcer',
        )
        # build_fake_bundle ships 3 IdentityAddon rows.
        self.assertEqual(
            preview['rows_by_model']['identity.identityaddon'], 3,
        )

    def test_preview_includes_code_and_media_trees(self):
        """Assert code_tree + media_tree reflect graft layout."""
        preview = loader.genome_preview('sourcer')

        self.assertIn(
            'are_self_fake_catalog/__init__.py', preview['code_tree'],
        )
        self.assertIn('avatar.png', preview['media_tree'])

    def test_preview_filters_pycache_from_trees(self):
        """Assert __pycache__ / .pyc are filtered from code_tree + media_tree."""
        code = self.grafts_root / 'sourcer' / 'code' / 'are_self_fake_catalog'
        cache = code / '__pycache__'
        cache.mkdir(exist_ok=True)
        (cache / 'leftover.cpython-314.pyc').write_bytes(b'\x00')
        media = self.grafts_root / 'sourcer' / 'media'
        (media / 'stray.pyc').write_bytes(b'\x00')

        preview = loader.genome_preview('sourcer')

        for entry in preview['code_tree']:
            self.assertNotIn('__pycache__', entry)
            self.assertFalse(entry.endswith('.pyc'))
        for entry in preview['media_tree']:
            self.assertFalse(entry.endswith('.pyc'))

    def test_preview_refuses_canonical(self):
        with self.assertRaisesRegex(ValueError, 'canonical'):
            loader.genome_preview(NeuralModifier.CANONICAL_SLUG)

    def test_preview_allows_incubator(self):
        """Assert INCUBATOR is in scope (real grafted genome)."""
        # Build a synthetic incubator graft so the preview has something
        # to read from. This mirrors what graft_incubator does at boot.
        incubator_graft = self.grafts_root / 'incubator'
        (incubator_graft / 'code' / 'incubator_genome').mkdir(parents=True)
        (
            incubator_graft / 'code' / 'incubator_genome' / '__init__.py'
        ).write_text('')
        (incubator_graft / 'media').mkdir()
        (incubator_graft / 'manifest.json').write_text(
            json.dumps({'slug': 'incubator', 'name': 'Incubator'}) + '\n',
        )
        incubator = NeuralModifier.objects.get(pk=NeuralModifier.INCUBATOR)
        incubator.status_id = NeuralModifierStatus.INSTALLED
        incubator.manifest_json = {
            'slug': 'incubator', 'name': 'Incubator',
        }
        incubator.save()

        preview = loader.genome_preview('incubator')
        self.assertEqual(preview['slug'], 'incubator')


